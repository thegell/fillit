#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	size_t	i;
	char	*ss;

	i = 0;
	if (s)
	{
		ss = (char*)s;
		while (ss[i])
		{
			ft_putchar_fd(ss[i], fd);
			i++;
		}
	}
}
